
public class Aula35 {

	public static void main(String[] args) {
		
		int a, b;
		String valor;
		a = 5;
		b = 6;
		
		/*
		 * Condição Utilisando o IF
		if (a == b) {
			valor = "Verdadeiro";
		}else {
			valor = "Falso";
			
		}
		
		System.out.println(valor);
		*/
		
		
		//Condição Utilizando Operação Ternária
		
		valor = (a>=b)? "Verdadeiro":"Falso";
		System.out.println(valor);
		
		
	}
}
